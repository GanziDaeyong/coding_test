import java.util.Arrays;
import java.util.Scanner;

public class 정올_방배정2 {

	public static void main(String[] args) {
		
		Scanner sc = new Scanner(System.in);
		int [] arr = new int [4];
		for (int i = 0 ; i < 4 ; i ++) {
			arr[i] = sc.nextInt();
		}
		Arrays.sort(arr, 0, 2);
		
		// 학생수에서 최댓값 배수 뺀 것들을 리스트에 담고 / 리스트 값들을 for i j 로 조합해서 되는지. i와 j는 0부터.
		
		
		

	}

}
